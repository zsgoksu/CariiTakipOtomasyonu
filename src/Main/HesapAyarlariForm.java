    private void btnKaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKaydetActionPerformed
        String kAdi = txtKadi.getText().trim();
        String kSifre1 = txtSifre.getText();
        String kSifre2 = txtSifreTekrar.getText();
        int yetki = cbYetki.getSelectedIndex();

        if (kAdi.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Kullanıcı adı girin...");
            txtKadi.requestFocus();
        } else if (kSifre1.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "şifre girin...");
            txtSifre.requestFocus();
        } else if (kSifre2.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "şifre tekrar girin...");
            txtSifre.requestFocus();
        }
        if (kSifre1.equals(kSifre2)) {
            try {
                System.out.println(yetki);
                String kaydetQuery = "insert into kullanicilar values(null,'" + kAdi + "','" + kSifre1 + "','" + yetki + "')";
                int sonuc = db.baglan().executeUpdate(kaydetQuery);
                if (sonuc > 0) {
                    txtKadi.setText("");
                    txtSifre.setText("");
                    txtSifreTekrar.setText("");

                    JOptionPane.showMessageDialog(rootPane, "Kayıt Başarılı");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Kayıt Başarısız");

                }
            } catch (Exception e) {
                System.err.println("kaydetme hatası : " + e);
                if (e.toString().contains("SQLITE_CONSTRAINT_UNIQUE")) {
                    JOptionPane.showMessageDialog(rootPane, "Bu kullanıcı adı zaten var...");
                }
            } finally {
                db.dbKapat();
                dataGetir();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Şifreler aynı olmalı");
            txtSifre.requestFocus();
        }

    }//GEN-LAST:event_btnKaydetActionPerformed
    String kdAdi;
    private void tblHesaplarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblHesaplarMouseClicked
        int row = tblHesaplar.getSelectedRow();
        if (row > -1) {
            kdAdi = (String) tblHesaplar.getValueAt(row, 0);
            txtKadi.setText(kdAdi);
            btnSil.setEnabled(true);
            btnDuzenle.setEnabled(true);
            btnKaydet.setEnabled(false);
        }
    }//GEN-LAST:event_tblHesaplarMouseClicked

    private void btnSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSilActionPerformed

        try {
            String silQuery = "delete from kullanicilar where kAdi = '" + kdAdi + "' ";
            int silSonuc = db.baglan().executeUpdate(silQuery);
            if (silSonuc > 0) {
                db.dbKapat();
                dataGetir();
                JOptionPane.showMessageDialog(rootPane, "Silme işlemi başarılı");
                txtKadi.setText("");
                txtSifre.setText("");
                txtSifreTekrar.setText("");
                btnSil.setEnabled(false);
                btnDuzenle.setEnabled(false);
                btnKaydet.setEnabled(true);
            } else {
                JOptionPane.showMessageDialog(rootPane, "Silme hatası !");
            }
        } catch (Exception e) {
            System.err.println("Silme hatası : " + e);
        } finally {
            db.dbKapat();

        }
    }//GEN-LAST:event_btnSilActionPerformed

    private void btnDuzenleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDuzenleActionPerformed
        String kAdi = txtKadi.getText().trim();
        String kSifre1 = txtSifre.getText();
        String kSifre2 = txtSifreTekrar.getText();
        int yetki = cbYetki.getSelectedIndex();

        if (kAdi.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Kullanıcı adı girin...");
            txtKadi.requestFocus();
        } else if (kSifre1.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "şifre girin...");
            txtSifre.requestFocus();
        } else if (kSifre2.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "şifre tekrar girin...");
            txtSifre.requestFocus();
        } else {
            if (kSifre1.equals(kSifre2) && !kSifre1.equals("")) {
                try {
                    String duzenleQuery = "update kullanicilar set kAdi = '" + kAdi + "', "
                            + "kSifre = '" + kSifre1 + "', kErisim = '" + yetki + "' where kAdi = '" + kdAdi + "'";
                    int sonuc = db.baglan().executeUpdate(duzenleQuery);
                    if (sonuc > 0) {
                        txtKadi.setText("");
                        txtSifre.setText("");
                        txtSifreTekrar.setText("");

                        JOptionPane.showMessageDialog(rootPane, "Kayıt Başarılı");
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "Kayıt Başarısız");

                    }

                } catch (SQLException ex) {
                    System.err.println("Yazma Hatası : " + ex);

                } finally {
                    db.dbKapat();
                }
                dataGetir();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Şifreler aynı olmalı");
                txtSifre.requestFocus();
            }
        }
    }//GEN-LAST:event_btnDuzenleActionPerformed

