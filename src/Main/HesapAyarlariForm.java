    public void dataGetir() {
        try {
            String urunlerQuery = "select *from kullanicilar";

            ResultSet rs = db.baglan().executeQuery(urunlerQuery);

            DefaultTableModel dtm = new DefaultTableModel();
            dtm.addColumn("Kullanici Adi");
            dtm.addColumn("Sifre");
            dtm.addColumn("Yetki");

            while (rs.next()) {
                int yetkiDuzenle = Integer.parseInt(rs.getString("kErisim"));
                String yetki;

                if (yetkiDuzenle == 2) {
                    yetki = "Mağza Müdürü";
                } else if (yetkiDuzenle == 1) {
                    yetki = "Muhasebeci";
                } else {
                    yetki = "Satış görevlisi";
                }
                dtm.addRow(new String[]{rs.getString("kAdi"), rs.getString("kSifre"), yetki});
            }
            tblHesaplar.setModel(dtm);

        } catch (Exception e) {
            System.err.println("okuma hatası...");
        } finally {
            db.dbKapat();
        }
    }

